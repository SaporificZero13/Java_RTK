/*Задача 1. Для введенной с клавиатуры буквы английского алфавита нужно вывести слева стоящую букву
на стандартной клавиатуре. При этом клавиатура замкнута, т.е. справа от буквы «p» стоит буква «a»,
а слева от "а" буква "р",  также соседними считаются буквы «l» и буква «z», а буква «m» с буквой «q».

Входные данные: строка входного потока содержит один символ — маленькую букву английского алфавита.

Выходные данные: следует вывести букву стоящую слева от заданной буквы, с учетом замкнутости клавиатуры.*/

import java.util.Scanner;

class KeyboardNeighbor {
    public static void main(String[] args) {
        // Создаем объект Scanner для чтения ввода с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Читаем введенный символ
        System.out.print("Введите букву английского алфавита: ");
        char inputChar = scanner.next().charAt(0);

        // Проверяем, что введенный символ - маленькая буква английского алфавита
        if (inputChar < 'a' || inputChar > 'z') {
            System.out.println("Ошибка: введен недопустимый символ");
            return;
        }

        // Создаем строку, представляющую последовательность букв на клавиатуре
        // в порядке их расположения (замкнутый круг)
        String keyboard = "qwertyuiopasdfghjklzxcvbnm";

        // Находим индекс введенной буквы в этой строке
        int index = keyboard.indexOf(inputChar);

        // Вычисляем индекс предыдущей буквы с учетом замкнутости клавиатуры
        int prevIndex = (index - 1 + keyboard.length()) % keyboard.length();

        // Получаем предыдущую букву
        char leftChar = keyboard.charAt(prevIndex);

        // Выводим результат
        System.out.println("Буква слева: " + leftChar);

        // Закрываем Scanner
        scanner.close();
    }
}